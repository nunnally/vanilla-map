const plotMarks = (results) =>{

    map.removeLayer(clusters);
    clusters = new L.FeatureGroup();

    colors ={
      '1':'orange',
      '2':'green',
      '3':'red',
      '4':'blue',
      '5':'black',
      '6':'purple'

    };
    results.forEach(result => {
        
        marker = L.circleMarker([result.latitude,result.longitude],{
            color:colors[result.caracteristica],
            radius:1,
            weight:5
        });
        marker.bindPopup(`
        <b>${result.escola}</b><br><br>
        <b>2019_ideb_1_5:</b> ${result['2019_ideb_1_5']}<br>
        <b>2019_ideb_6_9:</b> ${result['2019_ideb_6_9']}<br>
        <b>2019_ideb_em:</b> ${result['2019_ideb_em']}<br>
        `)
        clusters.addLayer(marker);
    });
    
    map.addLayer(clusters);
    return false;
}

